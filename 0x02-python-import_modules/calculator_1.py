#!/usr/bin/python3

def add(a, b):

    """My addition function

    Args:

    a: 10
    b: 5

    Returns:
        The return value. a + b
    """
    return (a + b)

def sub(a, b):

    """My subtraction function
    Args:

    a = 10
    b = 5

    Returns:
        The return value. a - b
    """
    return (a - b)

def mul(a, b):

    """My multiplication function

    Args:

    a = 10
    b = 5

    Returns:
        The return value. a * b
    """

    return (a * b)

def div(a, b):

    """My division function

    Args:

    a = 10
    b = 5

    Returns:
        The return value. a / b
    """

    return (a / b)
